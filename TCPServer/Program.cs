        {
            string Response="";
            try
            {
                if (ResponseList.TryGetValue(ESPMSG,out Response) == false)
                {
                    return "-1";
                }
                return Response;

            }
            catch (Exception e)
            {
                Console.WriteLine(e.ToString());
            }
